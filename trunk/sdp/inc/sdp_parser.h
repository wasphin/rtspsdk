#ifndef __SDP_PARSER__H__
#define __SDP_PARSER__H__

//
// Ensure that SDP_PARSER_DLL is default unless SDP_PARSER_STATIC is defined
//
#if defined(_WIN32) && defined(_DLL)
	#if !defined(SDP_PARSER_DLL) && !defined(SDP_PARSER_STATIC)
		#define SDP_PARSER_DLL
	#endif
#endif

//
// The following block is the standard way of creating macros which make exporting
// from a DLL simpler. All files within this DLL are compiled with the RTSP_SDK_EXPORTS
// symbol defined on the command line. this symbol should not be defined on any project
// that uses this DLL. This way any other project whose source files include this file see
// RTSP_SDK_API functions as being imported from a DLL, wheras this DLL sees symbols
// defined with this macro as being exported.
//
#if defined(_WIN32) && defined(SDP_PARSER_DLL)
	#if defined(SDP_EXPORTS)
		#define SDP_PARSER_API __declspec(dllexport)
	#else
		#define SDP_PARSER_API __declspec(dllimport)
	#endif
#endif


#if !defined(SDP_PARSER_API)
	#define SDP_PARSER_API
#endif


//
// Automatically link SDP parser library.
//
#if defined(_MSC_VER)
	#if !defined(POCO_NO_AUTOMATIC_LIBS) && !defined(SDP_EXPORTS)
		#if defined(SDP_PARSER_DLL)
			#if defined(_DEBUG)
				#pragma comment(lib, "sdpd.lib")
			#else
				#pragma comment(lib, "sdp.lib")
			#endif
/*
		#else
			#if defined(_DEBUG)
				#pragma comment(lib, "PocoNetmtd.lib")
			#else
				#pragma comment(lib, "PocoNetmt.lib")
			#endif
			*/
		#endif
	#endif
#endif


#endif // __SDP_PARSER__H__

